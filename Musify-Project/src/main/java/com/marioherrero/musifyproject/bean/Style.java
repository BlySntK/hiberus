package com.marioherrero.musifyproject.bean;

/**
 *
 * @author Mario Herrero
 */
public class Style {
    
    private int id;
    private String type;
    
    
    public Style () {}
    
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
